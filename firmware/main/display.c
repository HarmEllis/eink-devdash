static uint8_t bw_buf[EINK_BUF_SIZE];
static uint8_t red_buf[EINK_BUF_SIZE];

/* Set pixel (x=col 0-127, y=row 0-295). 0=black, 1=white */
static void set_pixel_bw(int x, int y, int black)
{
    if (x < 0 || x >= EINK_WIDTH || y < 0 || y >= EINK_HEIGHT) return;
    int idx = (y * EINK_WIDTH + x) / 8;
    int bit = 7 - ((y * EINK_WIDTH + x) % 8);
    if (black)
        bw_buf[idx] &= ~(1 << bit);
    else
        bw_buf[idx] |= (1 << bit);
}

static void set_pixel_red(int x, int y, int red)
{
    if (x < 0 || x >= EINK_WIDTH || y < 0 || y >= EINK_HEIGHT) return;
    int idx = (y * EINK_WIDTH + x) / 8;
    int bit = 7 - ((y * EINK_WIDTH + x) % 8);
    if (red)
        red_buf[idx] |= (1 << bit);   /* 1 = red for this panel */
    else
        red_buf[idx] &= ~(1 << bit);  /* 0 = white */
}

static void draw_char(int x, int y, char c, int use_red)
{
    if (c < 32 || c > 122) c = '?';
    const uint8_t *glyph = font5x7[c - 32];
    for (int col = 0; col < 5; col++) {
        for (int row = 0; row < 7; row++) {
            if (glyph[col] & (1 << row)) {
                if (use_red)
                    set_pixel_red(x + col, y + row, 1);
                else
                    set_pixel_bw(x + col, y + row, 1);
            }
        }
    }
}

static void draw_string(int x, int y, const char *s, int use_red)
{
    while (*s) {
        draw_char(x, y, *s++, use_red);
        x += FONT_W;
    }
}

static void draw_hline(int x, int y, int len)
{
    for (int i = 0; i < len; i++) set_pixel_bw(x + i, y, 1);
}

static eink_handle_t s_eink;
static bool s_initialized = false;

static void ensure_init(void)
{
    if (!s_initialized) {
        ESP_ERROR_CHECK(eink_init(&s_eink));
        s_initialized = true;
    }
}

void display_render(const dashboard_data_t *data, dash_config_t *cfg)
{
    ensure_init();
    memset(bw_buf, 0xFF, sizeof(bw_buf));
    memset(red_buf, 0x00, sizeof(red_buf));

    char line[64];

    /* Header */
    draw_string(2, 2, "DevDash", 1);
    draw_hline(0, 11, EINK_WIDTH);

    /* GitHub section */
    draw_string(2, 14, "GitHub", 0);
    snprintf(line, sizeof(line), "Issues: %d", data->github.issues);
    draw_string(2, 24, line, 0);
    snprintf(line, sizeof(line), "PRs:    %d", data->github.prs);
    draw_string(2, 34, line, 0);
    snprintf(line, sizeof(line), "Bots:   %d", data->github.dependabot);
    draw_string(2, 44, line, data->github.dependabot > 0);

    draw_hline(0, 54, EINK_WIDTH);

    /* Claude section */
    draw_string(2, 57, "Claude", 0);
    snprintf(line, sizeof(line), "5h: %d/%d", data->claude.five_hour.used, data->claude.five_hour.limit);
    draw_string(2, 67, line, data->claude.auth_error);
    snprintf(line, sizeof(line), "wk: %d/%d", data->claude.weekly.used, data->claude.weekly.limit);
    draw_string(2, 77, line, 0);

    draw_hline(0, 88, EINK_WIDTH);

    /* Codex section */
    draw_string(2, 91, "Codex", 0);
    snprintf(line, sizeof(line), "%d/%d today", data->codex.daily_used, data->codex.daily_limit);
    draw_string(2, 101, line, 0);

    draw_hline(0, 112, EINK_WIDTH);

    /* Status line */
    if (data->stale)
        draw_string(2, 115, "STALE", 1);
    else
        draw_string(2, 115, data->updated_at, 0);

    bool need_red = data->github.dependabot > 0 || data->claude.auth_error || data->stale;
    eink_refresh_mode_t mode;
    if (need_red) {
        mode = EINK_REFRESH_FULL_COLOR;
        cfg->last_red_state = true;
        cfg->bw_fast_cycle_count = 0;
    } else {
        cfg->bw_fast_cycle_count++;
        if (cfg->bw_fast_cycle_count >= 10) {
            mode = EINK_REFRESH_FULL_COLOR;
            cfg->bw_fast_cycle_count = 0;
        } else {
            mode = EINK_REFRESH_BW_FAST;
        }
        cfg->last_red_state = false;
    }

    eink_set_framebuffer(bw_buf, red_buf);
    eink_refresh(&s_eink, mode);
    eink_sleep(&s_eink);
}

void display_show_qr(void)
{
    ensure_init();
    memset(bw_buf, 0xFF, sizeof(bw_buf));
    memset(red_buf, 0x00, sizeof(red_buf));
    draw_string(2, 2, "Scan QR to configure", 0);
    draw_string(2, 14, "WiFi: devdash-prov", 0);
    eink_set_framebuffer(bw_buf, red_buf);
    eink_refresh(&s_eink, EINK_REFRESH_FULL_COLOR);
    eink_sleep(&s_eink);
}

void display_show_offline(void)
{
    ensure_init();
    memset(bw_buf, 0xFF, sizeof(bw_buf));
    memset(red_buf, 0x00, sizeof(red_buf));
    draw_string(2, 2, "OFFLINE", 1);
    draw_string(2, 14, "API unreachable", 0);
    eink_set_framebuffer(bw_buf, red_buf);
    eink_refresh(&s_eink, EINK_REFRESH_BW_FAST);
    eink_sleep(&s_eink);
}
